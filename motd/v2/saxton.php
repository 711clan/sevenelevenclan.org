                        <font size="5"><u>What is Vs Saxton Hale?</u></font><br /><br />
                        <strong>VS Saxton Hale Mod</strong> is a Juggernaut style mod of Arena that pits the TF2 mercenaries on RED against the Mann Co. CEO, Saxton Hale on BLU. Armed with plenty of health and his bare hands, the player taking control of Saxton Hale must eliminate all of RED to win, while they attempt to kill him.
                </div>
                    <h3><span id="Saxton_Hale">Saxton Hale</span></h3>
                    <ul>
                      <li><strong>Superjump</strong>: The player must crouch on the spot, look up and release crouch. This will send him flying up into the air. This ability has a 5 second cooldown.</li>
                      <li><strong>Rage</strong>: When Saxton Hale has taken enough damage, about 1,900, his rage meter will have been filled up. By taunting, all enemy players in close proximity will become stunned similar to how theGhosts in Harvest and the Horseless Headless Horsemann do on their respective Halloween maps. Saxton Hale's taunt however also affects Sentry Guns.</li>
                      <li><strong>Equalizer effect</strong>: move speed increases as Hale becomes injured.</li>
                    </ul>
                    <h3><span id="Vagineer">Vagineer</span></h3>
                    <ul>
                      <li><strong>Superjump</strong>: The player must crouch on the spot, look up and release crouch. This will send him flying up into the air. This ability has a 5 second cooldown.</li>
                      <li><strong>Rage</strong>: When Vagineer has taken enough damage, about 1,900, his rage meter will have been filled up. By taunting, Vagineer will become ÜberCharge-charged for 8.5 seconds. Vagineer's taunt slows the afflicted players movement, rather than stunning.</li>
                      <li><strong>Equalizer effect</strong>: move speed increases as Vagineer becomes injured.</li>
                    </ul>
                    <h3><span id="Horseless_Headless_Horsemann_Junior">Horseless Headless Horsemann Junior</span></h3>
                    <ul>
                      <li><strong>Teleport</strong>: The player must crouch on the spot, look up and release crouch. This will send him to random enemy. This ability has a 50 second cooldown.</li>
                      <li><strong>Rage</strong>: When HHHjr has taken enough damage, about 1,900, his rage meter will have been filled up. By taunting, all enemy players in close proximity will become stunned similar to how the Ghosts in Harvest and the Horseless Headless Horsemann do on their respective Halloween maps. HHHjr's taunt however also affects Sentry Guns.</li>
                      <li><strong>Equalizer effect</strong>: move speed increases as HHHjr becomes injured.</li>
                    </ul>
